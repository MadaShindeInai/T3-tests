import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useRouter } from "next/router";

const LocaleSwitcher = () => {
  const {
    locales,
    locale: activeLocale,
    pathname,
    query,
    asPath,
  } = useRouter();

  const otherLocales = (locales || []).filter(
    (locale) => locale !== activeLocale
  );

  return (
    <div className="body-bold flex items-center text-6xl text-white">
      <p className="body-bold mr-2">
        1. PRESS FR TO CHANGE LOCALE TO FR: {`=>`}
      </p>
      <ul className="body-bold flex">
        {otherLocales.map((locale) => {
          return (
            <li
              className="mr-2 border bg-white p-4 text-6xl text-red-900"
              key={locale}
            >
              <Link
                href={{ pathname, query }}
                as={asPath}
                locale={locale}
                legacyBehavior
              >
                {locale?.toUpperCase()}
              </Link>
            </li>
          );
        })}
      </ul>
    </div>
  );
};

const Home: NextPage = () => {
  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        <LocaleSwitcher />
        <div className="container flex flex-col items-center justify-center gap-12 px-4 py-16 ">
          <Link
            href="/protected"
            className="cursor-pointer bg-white p-4 text-6xl text-red-900 "
          >
            2. PRESS THIS TO CHECK AUTH ISSUE
          </Link>
        </div>
      </main>
    </>
  );
};

export default Home;
